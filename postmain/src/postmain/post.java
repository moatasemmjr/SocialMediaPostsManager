package postmain;

public abstract class post {

    // set and get text post 
    public abstract void settextpost(String text);

    public abstract String gettextpost();

    // set and get felling post 
    public abstract void setfelling(String felling);

    public abstract String getfelling();

    // set and get photo post 
    public abstract void setphoto(String photo);

    public abstract String getphoto();

    // set and get video post 
    public abstract void setvideo(String video);

    public abstract String getvideo();

    public abstract void setdate(String date);

    public abstract String getdata();

    public abstract void settime(String time);

    public abstract String gettime();

    public abstract void setusername(String username);

    public abstract String getusername();

} // end abstract posts 
